#include "header.h"

void	my_putnbr(int n)
{
	if (n < 0)
	{
		my_putchar('-');
		n = -n;
	}
	if (n == -2147483648)
	{
		my_putnbr(21474);
		my_putnbr(83648);
		return ;
	}
	if (n / 10)
		my_putnbr(n / 10);
	my_putchar(n % 10 + '0');
}
